  const listeners = new Set();

  function getNestedValue(object, key) {
    return key.split(".").reduce((value, part) => (value ? value[part] : undefined), object);
  }

  function interpolate(template, variables) {
    return template.replace(/\{(\w+)\}/g, (_, key) => {
      return Object.prototype.hasOwnProperty.call(variables, key) ? variables[key] : `{${key}}`;
    });
  }

  function getStoredLanguage() {
    try {
      const value = window.localStorage.getItem(STORAGE_KEY);
      return translations[value] ? value : "en";
    } catch (error) {
      return "en";
    }
  }

  let currentLanguage = getStoredLanguage();

  function getLanguageDirection(language) {
    return language === "ku" ? "rtl" : "ltr";
  }

  function t(key, variables) {
    const value = getNestedValue(translations[currentLanguage], key) ?? getNestedValue(translations.en, key) ?? key;
    if (typeof value !== "string") {
      return key;
    }
    return variables ? interpolate(value, variables) : value;
  }

  function applyTranslations(root) {
    const target = root || document;
    target.querySelectorAll("[data-i18n]").forEach((element) => {
      element.textContent = t(element.getAttribute("data-i18n"));
    });
  }

  function setLanguage(language) {
    if (!translations[language]) return;
    currentLanguage = language;
    document.documentElement.lang = language;
    document.documentElement.dir = getLanguageDirection(language);
    try {
      window.localStorage.setItem(STORAGE_KEY, language);
    } catch (error) {
      // Ignore storage errors.
    }
    applyTranslations(document);
    listeners.forEach((listener) => listener(language));
  }

  function onChange(listener) {
    listeners.add(listener);
    return () => listeners.delete(listener);
  }

  document.documentElement.lang = currentLanguage;
  document.documentElement.dir = getLanguageDirection(currentLanguage);

